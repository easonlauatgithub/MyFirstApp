package com.easontesting.myfirstapp;

public class PWOSName {
    private String name;
    private int status;
    public PWOSName(String name, int status) {
        this.name = name;
        this.status = status;
    }
    public String getName() {
        return name;
    }
    public int getStatus() {
        return status;
    }
}
